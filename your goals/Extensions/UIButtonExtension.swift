import UIKit

extension UIButton {

    func setSelectedColor() {
        self.backgroundColor = UIColor(red: 109/255, green: 188/255, blue: 99/255, alpha: 1)
    }

    func setDeselectedColor() {
        self.backgroundColor = UIColor(red: 178/255, green: 221/255, blue: 175/255, alpha: 1)
    }
}
